/*
Here's how the audio components need to be used:
1. You must always InitializeAudio before using anything else
2. Use LoadMusic (.mp3) or LoadEffect (.wav) to load an audio file
3. You can now use PlayMusic and PlayEffect to play an already loaded file
4. Remember to use SndShutdown to free audio ressources

Protip:
you can play a sound file with just one line by combining PlayMusic and LoadMusic all together:
PlayMusic(LoadMusic("NeverGonnaGiveYouUp.mp3"), 100, -1);
*/

#include <string>
using namespace std;

/////////////////////////////////////////////////////////////////////////////
// Represents a sound file's position in a vector																		   
/////////////////////////////////////////////////////////////////////////////
typedef int SndId;

/////////////////////////////////////////////////////////////////////////////
// InitializeAudio
// Initializes SDL2_Mixer components
// Params :	None
// Returns: None
/////////////////////////////////////////////////////////////////////////////
void InitializeAudio();



/////////////////////////////////////////////////////////////////////////////
// LoadMusic
// Loads an MP3 file into a vector
// Params :	FileName
// Returns: File ID number
/////////////////////////////////////////////////////////////////////////////
SndId LoadMusic(string FileName);


/////////////////////////////////////////////////////////////////////////////
// LoadEffect
// Loads a WAV file into a vector
// Params :	FileName
// Returns: File ID number
/////////////////////////////////////////////////////////////////////////////
SndId LoadEffect(string FileName);



/////////////////////////////////////////////////////////////////////////////
// PlayMusic
// Plays an MP3 file into from vector
// Params :	File ID Number
//			Volume (default 20)
//			Loops (default -1)
// Returns: None
/////////////////////////////////////////////////////////////////////////////
void PlayMusic(SndId music, int volume = 20, int loops = -1);



/////////////////////////////////////////////////////////////////////////////
// PlayEffect
// Plays a WAV file into from vector
// Params :	File ID Number
//			Volume (default 15)
//			Loops (default 0)
//			Channel (default -1)
// Returns: None
/////////////////////////////////////////////////////////////////////////////
void PlayEffect(SndId effect, int volume = 15, int loops = 0, int channel = -1);



/////////////////////////////////////////////////////////////////////////////
// SndShutdown
// Shuts down audio components of SDL2_mixer
// Params :	None
// Returns: None
/////////////////////////////////////////////////////////////////////////////
void SndShutdown();



/////////////////////////////////////////////////////////////////////////////
// PauseMusic
// Pauses any MP3 file playing
// Params :	None
// Returns: None
/////////////////////////////////////////////////////////////////////////////
void PauseMusic();
#endif // !AUDIO_H
